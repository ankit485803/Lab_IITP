26th Feb 2026 (Thursday) Lab5 on the FP Growth in dataMining



FP Growth (Frequent Pattern Growth) algorithm is a highly efficient data mining method for discovering frequent itemsets in large datasets without costly candidate generation, unlike the Apriori algorithm. It builds a compressed tree structure (FP-tree) to store data in just two scans. 
It is commonly used in retail for market basket analysis, such as identifying items often bought together




class FPNode:
    def __init__(self, item, count, parent):
        self.item = item          # item name
        self.count = count        # frequency
        self.parent = parent      # parent node
        self.children = {}        # child nodes
        self.link = None          # node link (header table)


# ---------------------------
# STEP 5: BUILD FP-TREE
# ---------------------------

def build_tree(dataset):
    root = FPNode(None, 1, None)
    header_table = {}

    for transaction in dataset:
        current_node = root

        for item in transaction:
            if item in current_node.children:
                current_node.children[item].count += 1
            else:
                new_node = FPNode(item, 1, current_node)
                current_node.children[item] = new_node

                # Maintain header table links
                if item in header_table:
                    current = header_table[item]
                    while current.link:
                        current = current.link
                    current.link = new_node
                else:
                    header_table[item] = new_node

            current_node = current_node.children[item]

    return root, header_table



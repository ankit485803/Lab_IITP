def find_prefix_path(base_item, header_table):
    paths = []
    node = header_table[base_item]

    while node:
        path = []
        parent = node.parent

        # Trace path to root
        while parent and parent.item:
            path.append(parent.item)
            parent = parent.parent

        # Add path according to node count
        for _ in range(node.count):
            if path:
                paths.append(path)

        node = node.link

    return paths

def mine_tree(header_table, prefix, frequency_patterns):

  for base_item in header_table:
    new_pattern = prefix.copy()
    new_pattern.add(base_item)
    frequent_patterns.append(new_pattern)

    conditional_pattern_base = find_prefix_path(base_item, header_table)
    conditional_tree, conditional_header = build_tree(conditional_pattern_base)

    if conditional_header:
      mine_tree(conditional_header, new_pattern, frequency_patterns)


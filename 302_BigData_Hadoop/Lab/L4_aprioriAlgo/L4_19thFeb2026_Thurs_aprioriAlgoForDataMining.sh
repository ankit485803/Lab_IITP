The Apriori algorithm is a foundational association rule learning method used to uncover relationships and hidden patterns 
within large datasets

The Apriori algorithm is an unsupervised machine learning algorithm used for association rule learning. 
Association rule learning is a data mining technique that identifies frequent patterns, connections and dependencies among different
 groups of items called itemsets in data.





